    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        String email = txtGmail.getText();
        String matKhau = txtMatKhauMoi.getText();
        if (email.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "email khong duoc de trong");
            return;
        }
        if (txtMa.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ma xac nhan khong duoc de trong");
            return;
        }
        lists = dvtk.getListTaiKhoan();
        TaiKhoan tk =dvtk.getOne(email);
        tk.setMatKhau(matKhau);
        if (txtMa.getText().equalsIgnoreCase("NSQODI")) {
            
            JOptionPane.showMessageDialog(this, dvtk.update(tk));
        }
        lists = dvtk.getListTaiKhoan();


    }//GEN-LAST:event_btnXacNhanActionPerformed

